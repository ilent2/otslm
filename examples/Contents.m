% OTSLM/EXAMPLES example files demonstrating different features
% of OTSLM.  For beam generation, start with simple_beams.m and
% advanced_beams.m.
%
% General examples
%   simple_beams         - Example of simple beams
%   advanced_beams       - Example of more advanced beams
%   dmd                  - Demonstrate the current DMD functionality of the toolbox
%   line_trap            - Simulation of different types of line traps
%   particle_tracking    - Simulate particles being imaged using SLM/QPD system
%   iterative            - Demonstrate different iterative algorithms
%   phaseblur            - Demonstration of effects of phase blur
%   iterative_3d         - Demonstration of 3-D iterative algorithms
%   gpu_demo             - Demonstration of the GPU capabilities of the toolbox
%   labview (folder)     - Shows toolbox can be controlled with labview
%   liveScripts (folder) - Shows toolbox control using Matlab livescripts
%
% +tools demonstrations
%   multi_beams          - Example to demonstrate combining multiple beams
%   region_sampling      - Demonstrating of otslm.tools.sample_region function
%   visualise            - Demonstrate different visualisation methods
%   tools (folder)       - Additional examples of ott.tools features
%
% +utils demonstrations
%   calibration          - Demonstrate the SLM calibration stuff
%   screen_device        - Demonstration of the screen device class (used to control SLM/DMDs)
%   imaging              - Demonstration of +utils/+imaging scripts
